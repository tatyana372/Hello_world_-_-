print("Картина 'Апофеоз войны' (Верещагин) — пирамида из черепов.")
print("Ты — полководец. Выбери путь:\n")

print("1 — Мир")
print("2 — Война")
print("3 — Отступление")

choice = input("\nТвой выбор (1, 2 или 3): ").strip()

print("\n--- РЕЗУЛЬТАТ ---")

if choice == "1" or choice == "мир":
    print("Ты выбрал мир. Кровь не пролита.")
    print("Пирамида из черепов осталась только на картине.")

elif choice == "2" or choice == "война":
    print("Ты выбрал войну. Города сожжены.")
    print("Пирамида из черепов — это твой памятник.")

elif choice == "3" or choice == "отступление":
    print("Ты отступил. Солдаты спасены.")
    print("Ты не герой, но люди живы.")

else:
    print("Ты не сделал выбор. Судьбу решили другие.")
